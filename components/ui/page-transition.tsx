"use client"

import { motion } from "framer-motion"

export const PageTransition = ({ children }: { children: React.ReactNode }) => {
    return (
        <motion.div
            initial={{ opacity: 0, y: 10 }}
            animate={{ opacity: 1, y: 0 }}
            exit={{ opacity: 0, y: -10 }}
            transition={{
                duration: 0.4,
                ease: [0.22, 1, 0.36, 1] // Custom quintic ease-out for a premium feel
            }}
        >
            {children}
        </motion.div>
    )
}
